//PRE INCREMENTO
let count = 0
console.log(++count)        // 1
console.log(count)          // 1

//POS INCREMENTO
let count = 0
console.log(count++)        // 0
console.log(count)          // 1


//PRE DECREMENTO
let count = 0
console.log(--count) // -1
console.log(count)  // -1

//POS DECREMENTO
let count = 0
console.log(count--) // 0
console.log(count)   // -1
